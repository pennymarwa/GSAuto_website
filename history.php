<p>Their lifetime knowledge, skills and experience was handed over by them to their next generation sons. The participation of Mr. Jasbir Singh Ryait (Current Chairman & CEO, G.S. Auto) and Mr. Surinder Singh Ryait (Current Managing Director, G.S. Auto) further catalyzed growth of the Company. The brothers complement and supplement each other perfectly, giving a great boost to the industry.</p>


