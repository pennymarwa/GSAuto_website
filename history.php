<h2> 
History of GS Auto
</h2>

<p>Our journey of excellence started long back in 1938 in pre independence days winen a young man Inaving a great vision translated his dream into reality steered by sheer hard work and determination.
The entrepreneurship of our founder, Baba Gurmukh Singh Ji, began with manufacturing of Bicycle Components, which afterwards got diversified into manufacturing of Automotive Components for various motor vehicles.</p>

 
<p>Further momentum was gained with the joining of his sons Giani Bhagat Singh (Former Chairman-G.S. Group) and S. Jagat Singh (Former Managing Director-G.S. Group). S. Jagat Singh had great marketing skills and vision to make GS as top brand in auto component market of India. He formed a small team of 4 dedicated people to start marketing activity outside Punjab across India.
They remained for months out of their home town traveling all small and major towns of India to select dealers and distributors & formed formidable and unparalleled pan India network of more than 500 Distributors and 10,000 Retailers.</p>
