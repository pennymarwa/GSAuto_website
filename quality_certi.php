<h2> Quality Policy
</h2>
<p>
From the very beginning, G.S. Auto has always shown incessant thirst for Product Quality and Customer Satisfaction. At G.S., each component passes through series of stringent tests of quality from Design Stage to Manufacturing. Conformance to the Quality is just not restricted to the shop floor of the company but also throughout the supply chain.</p>


<p>After having conferred ISO 9002 certification in 1997 and QS 9000 certification in 1999 (re-certified in 2003) by BVQI, U.K. The company looked forward to take further leaps by implementation of 5S, TPM, TQM concepts i.e. Total Productive Maintenance/Total Quality ManageÂ¬ ment and Lean Manufacturing. The Management of G.S. Auto feels that these are the essential ingredients of success & sustenance in the present times due to fierce competition offered by Global Companies. The Quality Movement in G.S. Auto has achieved its next milestone- ISO/TS 16949 accreditation in April 2005. Which has been re-certified numerous times till now.</p>

<p>Following Quality System requirements are always taken care at G.S. Auto for conformance to Quality Standards, On-time Delivery, Maintaining Product Quality and Handling Customer's Complaint:
<ol>
<li>1) APQP (Advanced Product Quality Planning).</li>
<li>2) PPAP (Production Part Approval Process).</li>
<li>3) FMEA (Failure Mode Effect Analysis).</li>
<li>4) SPC (Statistical Process Control).</li>
<li>5) MSA (Measurement System Analysis).</li>
</ol>
</p>
